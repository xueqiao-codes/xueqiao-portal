package com.longsheng.xueqiao.admin.bean;

public class LocalExchangeMapping {

	private int mappingId;
	private String sledExchangeMic;
	private String sledExchangeCnName;
	private String techPlatform;
	private String techPlatformExchangeCode;

	public int getMappingId() {
		return mappingId;
	}

	public void setMappingId(int mappingId) {
		this.mappingId = mappingId;
	}

	public String getSledExchangeMic() {
		return sledExchangeMic;
	}

	public void setSledExchangeMic(String sledExchangeMic) {
		this.sledExchangeMic = sledExchangeMic;
	}

	public String getSledExchangeCnName() {
		return sledExchangeCnName;
	}

	public void setSledExchangeCnName(String sledExchangeCnName) {
		this.sledExchangeCnName = sledExchangeCnName;
	}

	public String getTechPlatform() {
		return techPlatform;
	}

	public void setTechPlatform(String techPlatform) {
		this.techPlatform = techPlatform;
	}

	public String getTechPlatformExchangeCode() {
		return techPlatformExchangeCode;
	}

	public void setTechPlatformExchangeCode(String techPlatformExchangeCode) {
		this.techPlatformExchangeCode = techPlatformExchangeCode;
	}

}
