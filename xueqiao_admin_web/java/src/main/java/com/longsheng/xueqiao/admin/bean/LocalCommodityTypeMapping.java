package com.longsheng.xueqiao.admin.bean;

public class LocalCommodityTypeMapping {
	private int mappingId;
	private String sledCommodityType;
	private String techPlatform;
	private String techPlatformCommodityType;

	public int getMappingId() {
		return mappingId;
	}

	public void setMappingId(int mappingId) {
		this.mappingId = mappingId;
	}

	public String getSledCommodityType() {
		return sledCommodityType;
	}

	public void setSledCommodityType(String sledCommodityType) {
		this.sledCommodityType = sledCommodityType;
	}

	public String getTechPlatform() {
		return techPlatform;
	}

	public void setTechPlatform(String techPlatform) {
		this.techPlatform = techPlatform;
	}

	public String getTechPlatformCommodityType() {
		return techPlatformCommodityType;
	}

	public void setTechPlatformCommodityType(String techPlatformCommodityType) {
		this.techPlatformCommodityType = techPlatformCommodityType;
	}
}
